public class MiniParser {

    public NestedInteger deserialize(String s) {

 		Stack<Character> st = new Stack<Character>();
 		NestedInteger result = new NestedInteger();
 		String num="";
 		
 		if (s.equals("")) return result;

 		else if (s.charAt(0)=='[' && s.charAt(s.length()-1)==']') 
 			return deserialize(s.substring(1, s.length()-1));

 		for (int i=0; i<=s.length(); i++) {
 			if (i < s.length() && s.charAt(i)=='[') {
 				int mark=i;
 				st.push('[');
 				while (!st.empty() || i<s.length()) {
 					i++;
 					if (s.charAt(i)=='[') st.push('[');
 					else if (s.charAt(i)==']') st.pop();
 				}
 				result.add(deserialize(s.substring(mark, i)));
 			}

 			else if ((i < s.length() && (s.charAt(i) == '-' || s.charAt(i) == ',')) 
 					|| (i == s.length() && num.length() > 0)) {
 				result.setInteger(Integer.parseInt(num));
 				num="";
 			}

 			else {
 				num+=s.charAt(i);
 			}
 		}       

 		return result;
    }
}
